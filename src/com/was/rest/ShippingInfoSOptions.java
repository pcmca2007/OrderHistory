public class ShippingInfoSOptions extends ShippingOptions {
	
	
	private String name;
	private String address1;
	private String address2;
	private String city;
	private String state;
	private String zip;
	private String ship_to_phone;
	private String ship_type;
	
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress1() {
		return address1;
	}
	public void setAddress1(String address1) {
		this.address1 = address1;
	}
	public String getAddress2() {
		return address2;
	}
	public void setAddress2(String address2) {
		this.address2 = address2;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getShip_to_phone() {
		return ship_to_phone;
	}
	public void setShip_to_phone(String ship_to_phone) {
		this.ship_to_phone = ship_to_phone;
	}
	public String getShip_type() {
		return ship_type;
	}
	public void setShip_type(String ship_type) {
		this.ship_type = ship_type;
	}
	
	
	

}
 